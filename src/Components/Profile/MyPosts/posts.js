import React from "react";
import NewPost from "./post";

function MyPosts(props) {
    // debugger
    let sortedPosts = props.state.postsReducer.Posts
        .map( post => <NewPost
            id={post.id}
            name={post.name}
            text={post.text}
            pict={post.ava}
            likes={post.likes}
            dispatch={props.dispatch}
        />)
        .reverse();
    return(
        <div>
            {sortedPosts}
        </div>
    )
}

export default MyPosts